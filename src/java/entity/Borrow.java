package entity;

/**
 *
 * @author admin
 */
public class Borrow {
    private int id;
    private Customer customer;
    private Cart cart;

    public Borrow() {
    }

    public Borrow(int id, Customer customer, Cart cart) {
        this.id = id;
        this.customer = customer;
        this.cart = cart;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Customer getCustomer() {
        return customer;
    }

    public void setCustomer(Customer customer) {
        this.customer = customer;
    }

    public Cart getCart() {
        return cart;
    }

    public void setCart(Cart cart) {
        this.cart = cart;
    }
    
}
