package model;

/**
 *
 * @author admin
 */
public class AuthorBook {
    private int id;
    private Author author;
    private Book book;

    public AuthorBook() {
    }

    public AuthorBook(int id, Author author, Book book) {
        this.id = id;
        this.author = author;
        this.book = book;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Author getAuthor() {
        return author;
    }

    public void setAuthor(Author author) {
        this.author = author;
    }

    public Book getBook() {
        return book;
    }

    public void setBook(Book book) {
        this.book = book;
    }
    
}
